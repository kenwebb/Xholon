	/**
	 * Transform IXholon (IApplication) node into an XML String containing params.
	 * @param xhNode
	 * @param xholon2Xml
	 * @return
	 */
	public String xholon2XmlString(IXholon xhNode, IXholon2Xml xholon2Xml)
	{
		//Writer w = new StringWriter();
		StringBuilder w = new StringBuilder();
		IXmlWriter xmlWriter = XmlWriterFactory.getXmlWriter(w);
		if (xholon2Xml.isWriteStartDocument()) {
			xmlWriter.writeStartDocument();
		}
		xhNode.toXmlAttributes(xholon2Xml, xmlWriter);
		xmlWriter.writeEndDocument();
		xmlWriter.flush();
		return w.toString();
	}

